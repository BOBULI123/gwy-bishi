export function getRealQuestionSources(examType?: "xingce" | "shenlun") {
  const sources = examType ? realQuestionSources.filter((source) => source.examType === examType) : realQuestionSources;
  return [...sources].sort((a, b) => b.year - a.year || a.title.localeCompare(b.title, "zh-CN"));
}

export function searchRealQuestionSources(keyword: string, examType?: "xingce" | "shenlun") {
  const normalized = keyword.trim().toLowerCase();
  const sources = getRealQuestionSources(examType);
  if (!normalized) return sources;
  return sources.filter((source) =>
    `${source.title}${source.year}${source.province}${source.category}${source.sourceName}${source.sourceNote}`
      .toLowerCase()
      .includes(normalized),
  );
}
